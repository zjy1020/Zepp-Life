def zepp_update_steps(user, password, steps):
    """执行完整的 Zepp Life 刷步流程，返回 (success, message, log_text)"""
    log = []
    device_id = str(uuid.uuid4())

    log.append('初始化 Runner 完成')
    log.append(f'设备ID: {device_id}')

    is_phone = user.startswith('+86') or '@' not in user
    log.append(f'账号类型: {"手机号" if is_phone else "邮箱"}')

    # ---------- 登录第一步：AES 加密 POST ----------
    login_data = {
        'emailOrPhone': user,
        'password': password,
        'state': 'REDIRECTION',
        'client_id': 'HuaMi',
        'country_code': 'CN',
        'token': 'access',
        'redirect_uri': 'https://s3-us-west-2.amazonaws.com/hm-registration/successsignin.html',
    }

    query_str = '&'.join(f'{k}={requests.utils.quote(str(v), safe="")}' for k, v in login_data.items())
    plain_bytes = query_str.encode('utf-8')

    if HAS_CRYPTO:
        encrypted = aes_encrypt(plain_bytes)
    else:
        return (False, '缺少 pycryptodome 库，无法加密', '\n'.join(log))

    log.append(f'登录参数加密完成，长度: {len(encrypted)}')

    headers1 = {
        'content-type': 'application/x-www-form-urlencoded; charset=UTF-8',
        'user-agent': 'MiFit6.14.0 (M2007J1SC; Android 12; Density/2.75)',
        'app_name': 'com.xiaomi.hm.health',
        'appname': 'com.xiaomi.hm.health',
        'appplatform': 'android_phone',
        'x-hm-ekv': '1',
        'hm-privacy-ceip': 'false',
    }

    r1 = None
    for attempt in range(2):
        try:
            resp = requests.post(
                'https://api-user.zepp.com/v2/registrations/tokens',
                headers=headers1,
                data=encrypted,
                allow_redirects=False,
                timeout=15,
            )
            if resp.status_code == 303:
                r1 = resp
                break
            if resp.status_code == 429:
                log.append(f'v2登录限流(429)，第{attempt + 1}次重试')
                continue
            log.append(f'v2登录异常，状态码: {resp.status_code}')
            log.append(f'响应片段: {resp.text[:120]}')
            return (False, '登录第一步失败', '\n'.join(log))
        except requests.RequestException as e:
            log.append(f'v2登录请求异常: {e}')

    if not r1:
        return (False, '登录失败，未获取重定向', '\n'.join(log))

    location = r1.headers.get('Location', '')
    import re
    m = re.search(r'access=([^&]+)', location)
    if not m:
        return (False, '获取 accessToken 失败', '\n'.join(log))
    code = m.group(1)
    log.append('登录第一步成功，Location 解析完成')

    # ---------- 登录第二步：获取 login_token ----------
    headers2 = {
        'app_name': 'com.xiaomi.hm.health',
        'x-request-id': str(uuid.uuid4()),
        'accept-language': 'zh-CN',
        'appname': 'com.xiaomi.hm.health',
        'cv': '50818_6.14.0',
        'v': '2.0',
        'appplatform': 'android_phone',
        'content-type': 'application/x-www-form-urlencoded; charset=UTF-8',
    }

    if is_phone:
        data2 = {
            'app_name': 'com.xiaomi.hm.health',
            'app_version': '6.14.0',
            'code': code,
            'country_code': 'CN',
            'device_id': device_id,
            'device_model': 'phone',
            'grant_type': 'access_token',
            'third_name': 'huami_phone',
        }
    else:
        data2 = {
            'allow_registration=': 'false',
            'app_name': 'com.xiaomi.hm.health',
            'app_version': '6.14.0',
            'code': code,
            'country_code': 'CN',
            'device_id': device_id,
            'device_model': 'android_phone',
            'dn': 'account.zepp.com,api-user.zepp.com,api-mifit.zepp.com,api-watch.zepp.com,app-analytics.zepp.com,api-analytics.huami.com,auth.zepp.com',
            'grant_type': 'access_token',
            'lang': 'zh_CN',
            'os_version': '1.5.0',
            'source': ' com.xiaomi.hm.health:6.14.0:50818',
            'third_name': 'email',
        }

    try:
        r2 = requests.post(
            'https://account.huami.com/v2/client/login',
            headers=headers2,
            data=data2,
            timeout=15,
        )
    except requests.RequestException as e:
        return (False, f'登录第二步请求失败: {e}', '\n'.join(log))

    if r2.status_code != 200:
        log.append(f'获取login_token失败，HTTP状态码: {r2.status_code}')
        log.append(f'响应内容: {r2.text[:200]}')
        return (False, '登录第二步失败', '\n'.join(log))

    try:
        r2json = r2.json()
    except Exception:
        return (False, '解析登录响应 JSON 失败', '\n'.join(log))

    if 'token_info' not in r2json:
        return (False, '响应缺少 token_info 字段', '\n'.join(log))

    login_token = r2json['token_info']['login_token']
    user_id = r2json['token_info']['user_id']
    log.append('登录第二步成功，login_token 与 userid 获取完成')

    # ---------- 第三步：获取 app_token ----------
    url3 = (
        f'https://account-cn.huami.com/v1/client/app_tokens'
        f'?app_name=com.xiaomi.hm.health'
        f'&dn=api-user.huami.com%2Capi-mifit.huami.com%2Capp-analytics.huami.com'
        f'&login_token={login_token}'
    )
    try:
        r3 = requests.get(url3, headers={
            'User-Agent': 'MiFit/5.3.0 (iPhone; iOS 14.7.1; Scale/3.00)'
        }, timeout=15)
        r3json = r3.json()
    except Exception as e:
        return (False, f'获取 app_token 失败: {e}', '\n'.join(log))

    app_token = r3json.get('token_info', {}).get('app_token')
    if not app_token:
        return (False, 'app_token 为空', '\n'.join(log))
    log.append('app_token 获取成功')

    # ---------- 第四步：提交步数 ----------
    today = get_today()
    step_str = str(steps)

    data_json = DATA_JSON_TPL.replace('2021-08-07', today)
    # 替换 ttl 步数值
    data_json = re.sub(r'(ttl%5C%22%3A)\d+', r'\g<1>' + step_str, data_json)

    import time
    t = str(int(time.time() * 1000))
    post_data = (
        f'userid={user_id}'
        f'&last_sync_data_time=1597306380'
        f'&device_type=0'
        f'&last_deviceid=DA932FFFFE8816E7'
        f'&data_json={data_json}'
    )

    try:
        r4 = requests.post(
            f'https://api-mifit-cn.huami.com/v1/data/band_data.json?&t={t}',
            headers={
                'apptoken': app_token,
                'Content-Type': 'application/x-www-form-urlencoded',
            },
            data=post_data,
            timeout=15,
        )
        r4json = r4.json()
    except Exception as e:
        return (False, f'提交步数失败: {e}', '\n'.join(log))

    log.append(f'修改步数（{step_str}）[{r4json.get("message", "未知")}]')
    return (True, f'修改成功！当前步数: {step_str}', '\n'.join(log))


